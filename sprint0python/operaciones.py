#Suma
def suma(a, b):
    return a + b

#Resta
def resta(a, b):
    return a - b

#Multiplicación
def multiplicacion(a, b):
    return a * b

#División
def division(a, b):
    if b == 0:
        return "Error: no se puede dividir entre 0."
    return a / b


